package tamrin1.session7.annotation;

/**
 * This is a <b>Calculator</b>
 * @author Maryam
 * @see Calculator
 */

public class Calculator {
	/**
	 * This method adds 2 numbers. 
	 * @param a	Enter first number.
	 * @param b	Enter second number.
	 * @return	sum of a, b
	 */
	public static int add(int a, int b) {
		return a + b;
	}
	
	/**
	 * This method subtract 2 numbers. 
	 * @param a	Enter first number.
	 * @param b	Enter second number.
	 * @return	subtraction of a, b
	 */
	public static int multi(int a, int b) {
		return a * b;
	}
}
